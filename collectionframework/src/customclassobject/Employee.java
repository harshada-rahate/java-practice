package customclassobject;

public class Employee {
	private int id;
	private String name;
	private float salary;
	
	
	public void SetId(int id)
	{
		this.id=id;
	}
	public void SetName(String name)
	{
		this.name=name;
	}
	public void SetSalary(float salary)
	{
		this.salary=salary;
	}
	public int getId()
	{
		return id ;
	}
	public String getName()
	{
		return name;
	}
	public float getSalary()
	{
		return salary;
	}
	
		
	}
